    private void btnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateActionPerformed
        // TODO add your handling code here:
        
        if(txtQuantity.getText().isEmpty() || cbxProduct.getSelectedIndex() == 0 || txtName.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Fill All Inputs!!!!", "Error", HEIGHT);
        }
        
        else{
            quantity = Double.parseDouble(txtQuantity.getText());
            //tax  = Double.parseDouble(txtTax.getText());
            ProductSelect(); // Selection of products the customer bought
        
            tax = quantity * productPrice * 0.03; //Calculation for tax
            total = quantity * productPrice + tax; //Calculation for the total purchase

            txtTotal.setText(String.valueOf(total)); // Displays the total
            txtTax.setText(String.format("%.2f", tax)); // Displays the tax
            
        
        }
        
    }//GEN-LAST:event_btnCalculateActionPerformed

