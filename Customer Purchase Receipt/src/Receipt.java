    public void ProductSelect(){
        
        if(cbxProduct.getSelectedItem() == "Shirts")
        {
            productPrice = 30;
        }
        
        if(cbxProduct.getSelectedItem() == "Trousers")
        {
            productPrice = 50;
        }
        
        if(cbxProduct.getSelectedItem() == "Ties")
        {
            productPrice = 10;
        }
        
        if(cbxProduct.getSelectedItem() == "Shoes")
        {
            productPrice = 80;
        }
        
        if(cbxProduct.getSelectedItem() == "Suits" )
        {
            productPrice = 230;
        }
        
    }
    
    public void SaveDetails() throws IOException{
        String filename = txtName.getText() + ".txt"; //Saves file as your name
        
        //Classes for writing and saving a file
        FileWriter filewriter = new FileWriter(filename,true);
        PrintWriter output  = new PrintWriter(filewriter);
        
        output.println("**************************************");
        output.println("Name: " +txtName.getText());
        output.println("Product: " +cbxProduct.getSelectedItem());
        output.println("Price: Ghc." +productPrice);
        output.println("Quantity: " +quantity);
        output.println("Tax(3%): Ghc." +tax);
        output.println("Total: Ghc." +total);
        output.println("**************************************");
        output.close();
        
        JOptionPane.showMessageDialog(this, "Details Saved!!");
    }

